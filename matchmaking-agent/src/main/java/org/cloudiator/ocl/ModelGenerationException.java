package org.cloudiator.ocl;

public class ModelGenerationException extends Exception {

  public ModelGenerationException() {
  }

  public ModelGenerationException(String s) {
    super(s);
  }

  public ModelGenerationException(String s, Throwable throwable) {
    super(s, throwable);
  }

  public ModelGenerationException(Throwable throwable) {
    super(throwable);
  }

  public ModelGenerationException(String s, Throwable throwable, boolean b, boolean b1) {
    super(s, throwable, b, b1);
  }
}
